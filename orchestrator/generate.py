#!/usr/bin/env python3
"""
ICS-SimLab Extended — Orchestrator

Reads ctf-config.yaml and generates:
  infrastructure/networks/docker-compose.yml   shared zone networks (bring up first)
  zones/enterprise/docker-compose.yml
  zones/operational/docker-compose.yml
  zones/control/docker-compose.yml             9 custom containers (turbine PLC, HMI, IEDs, actuators)
  start.sh
  stop.sh

Usage:
    python orchestrator/generate.py [ctf-config.yaml]
"""

