INFRA_DIR = REPO_ROOT / "infrastructure"
FIREWALL_RULES = ORCHESTRATOR_DIR / "firewall-rules.txt"
ADVERSARY_README = ORCHESTRATOR_DIR / "adversary-readme.txt"

